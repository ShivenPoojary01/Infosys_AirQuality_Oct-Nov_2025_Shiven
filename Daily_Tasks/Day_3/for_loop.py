# Tuple with for loop
numbers = (10, 20, 30, 40)
print("Tuple Iteration:")
for n in numbers:
    print(n)

# Dictionary with for loop
student = {"name": "Alice", "age": 20, "course": "CS"}
print("\nDictionary Iteration (keys and values):")
for key, value in student.items():
    print(key, ":", value)

# Set with for loop
fruits = {"apple", "banana", "cherry"}
print("\nSet Iteration:")
for f in fruits:
    print(f)
